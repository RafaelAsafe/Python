n = input('digite algo:\n')
print(n.isnumeric())
# mostra se o valor pode ser transformado em numero ou é um numero
print(n.isalpha())
# mostra se a coisa inserida é alfabetica
print(n.isupper())
# mostra se a coisa inserida está escrita somente em maiúsculo
print(n.isdigit())
print(n.isalnum())
print(n.isspace())
print(n.islower())
print(n.istitle())
print(n.isidentifier)
print(n.isprintable)
print(n.isdecimal)
print(n.isascii)
